# ── Автогенерируемые структуры ───────────────────────────────────────


def get_hierarchy() -> dict:
    """doc_type → порядок внутри группы (для sort_order)."""
    result = {row[0]: row[3] for row in DOC_TYPES}
    # Все «Договор <подвид>» получают порядок как у «Договор»
    base_order = result.get("Договор", 1)
    for subtype in CONTRACT_SUBTYPES:
        result[f"Договор {subtype}"] = base_order
    return result


def get_type_to_category() -> dict:
    """doc_type → (категория, подкатегория)."""
    result = {}
    for row in DOC_TYPES:
        name, cat, subcat, _ = row
        if cat and subcat:
            result[name] = (cat, subcat)
        elif cat:
            result[name] = (cat, "")
    # Все «Договор <подвид>» → та же категория что у «Договор»
    base = result.get("Договор")
    if base:
        for subtype in CONTRACT_SUBTYPES:
            result[f"Договор {subtype}"] = base
    return result


def is_contract_subtype(doc_type: str) -> bool:
    """True если тип является подвидом договора (напр. «Договор поставки»)."""
    if not doc_type:
        return False
    for subtype in CONTRACT_SUBTYPES:
        if doc_type.lower() == f"договор {subtype}".lower():
            return True
    return False


def resolve_base_type(doc_type: str) -> str:
    """Возвращает базовый тип: «Договор поставки» → «Договор»."""
    if is_contract_subtype(doc_type):
        return "Договор"
    return doc_type


def get_prompt_doc_types() -> str:
    """Генерирует строку со списком типов для ANALYSIS_PROMPT."""
    types = []
    for row in DOC_TYPES:
        name = row[0]
        if name == "Прочее":
            continue
        types.append(name)
    # Подвиды договоров
    for subtype in CONTRACT_SUBTYPES:
        types.append(f"Договор {subtype}")
    types.append("Прочее")
    return " / ".join(types)
